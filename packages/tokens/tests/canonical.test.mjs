test("generated token docs are consumable without scraping CSS", () => {
  const semanticTextGroup = tokenDocs.groups.find((group) => group.name === "color.semantic.text");
  assert.ok(semanticTextGroup);
  assert.ok(
    semanticTextGroup.tokens.some(
      (token) =>
        token.name === "color.semantic.text.default" &&
        token.cssVariable === "--ds-color-text-default" &&
        token.value.light === "#083344" &&
        token.value.dark === "#FDFDFD"
    )
  );
  const shadowGroup = tokenDocs.groups.find((group) => group.name === "shadow.card");
  assert.ok(shadowGroup);
  assert.ok(
    shadowGroup.tokens.some(
      (token) =>
        token.name === "shadow.card" &&
        token.cssVariable === "--ds-shadow-card" &&
        token.value.light.blur === 8 &&
        token.value.dark.blur === 8
    )
  );
});

test("generated token outputs are deterministic", async () => {
  const before = await hashDistFiles();
  execFileSync(
    "node",
    [
      "../token-pipeline/dist/cli/buildCanonical.js",
      "./fixtures/extracted",
      "./dist",
      "../../token-pipeline.config.json"
    ],
    {
      cwd: new URL("..", import.meta.url),
      stdio: "pipe"
    }
  );
  const after = await hashDistFiles();

  assert.deepEqual(after, before);
});

async function hashDistFiles() {
  const distUrl = new URL("../dist/", import.meta.url);
  const files = (await readdir(distUrl)).filter((file) =>
    [
      "build-report.json",
      "canonical.json",
      "index.d.ts",
      "index.js",
      "metadata.json",
      "token-docs.json",
      "token-quality.json",
      "token-quality.md",
      "token-names.d.ts",
      "token-names.js",
      "tokens.css",
      "tokens.dark.css",
      "tokens.light.css"
    ].includes(file)
  );

  const hashes = {};
  for (const file of files.sort()) {
    const contents = await readFile(new URL(file, distUrl));
    hashes[file] = createHash("sha256").update(contents).digest("hex");
  }
  return hashes;
}
