test("generated canonical JSON contains core token groups", () => {
  assert.ok(canonical.tokens.color.primitive);
  assert.ok(canonical.tokens.color.semantic);
  assert.ok(canonical.tokens.space);
  assert.ok(canonical.tokens.radius);
  assert.ok(canonical.tokens.shadow);
  assert.ok(canonical.tokens.typography);
});

test("semantic colour tokens contain light and dark mode values", () => {
  assert.deepEqual(Object.keys(canonical.tokens.color.semantic.text.default.value).sort(), [
    "dark",
    "light"
  ]);
});

test("canonical JSON does not contain source-only forbidden markers", () => {
  const serialized = JSON.stringify(canonical);
  for (const marker of [
    "$extensions",
    "com.figma",
    "VariableID",
    "VariableCollectionId",
    "targetVariable",
    "PRIVATE_COMPANY_NAME_PLACEHOLDER"
  ]) {
    assert.equal(serialized.includes(marker), false, `${marker} should not be present`);
  }
});

